package cn.uway.nbi.validate;

public class CheckMapping extends CommonFunction {

	private String name;

	private String level;

	private String ne_sys_id;

	public String getLevel() {
		return level;
	}

	public void setLevel(String level) {
		this.level = level;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getNe_sys_id() {
		return ne_sys_id;
	}

	public void setNe_sys_id(String ne_sys_id) {
		this.ne_sys_id = ne_sys_id;
	}
}
