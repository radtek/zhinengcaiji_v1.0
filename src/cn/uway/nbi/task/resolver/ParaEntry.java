package cn.uway.nbi.task.resolver;

public class ParaEntry {

	private String paraEn;

	private String format;

	private String rawValue;

	private String convertValue;

	public String getParaEn() {
		return paraEn;
	}

	public void setParaEn(String paraEn) {
		this.paraEn = paraEn;
	}

	public String getFormat() {
		return format;
	}

	public void setFormat(String format) {
		this.format = format;
	}

	public String getRawValue() {
		return rawValue;
	}

	public void setRawValue(String rawValue) {
		this.rawValue = rawValue;
	}

	public String getConvertValue() {
		return convertValue;
	}

	public void setConvertValue(String convertValue) {
		this.convertValue = convertValue;
	}

}
