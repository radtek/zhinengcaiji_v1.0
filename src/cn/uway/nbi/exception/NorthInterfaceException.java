public class NorthInterfaceException extends Exception {

	private static final long serialVersionUID = 151731790606562035L;

	public NorthInterfaceException() {
		super();
	}

	public NorthInterfaceException(String message, Throwable cause) {
		super(message, cause);
	}

	public NorthInterfaceException(String message) {
		super(message);
	}

	public NorthInterfaceException(Throwable cause) {
		super(cause);
	}

}